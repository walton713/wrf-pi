#!/usr/bin/env bash

RED=$'\e[0;31m'
GREEN=$'\e[0;32m'
NC=$'\e[0m'

print_block_message() {
  echo ""
  echo ""
  echo -e "${GREEN}####################################################################################################${NC}"
  echo -e "${GREEN}####################################################################################################${NC}"
  echo -e "${GREEN}#${NC}"
  echo -e "${GREEN}#${NC}"
  echo -e "${GREEN}# $1${NC}"
  echo -e "${GREEN}#${NC}"
  echo -e "${GREEN}#${NC}"
  echo -e "${GREEN}####################################################################################################${NC}"
  echo -e "${GREEN}####################################################################################################${NC}"
  echo ""
  echo ""
}

print_update_message() {
  echo -e "${GREEN}$1${NC}"
}

print_error_message() {
  echo -e "${RED}$1${NC}"
}

#!/usr/bin/env bash

print_block_message() {
  echo ""
  echo ""
  echo -e "${GREEN}####################################################################################################${NC}"
  echo -e "${GREEN}####################################################################################################${NC}"
  echo -e "${GREEN}#${NC}"
  echo -e "${GREEN}#${NC}"
  echo -e "${GREEN}# $1${NC}"
  echo -e "${GREEN}#${NC}"
  echo -e "${GREEN}#${NC}"
  echo -e "${GREEN}####################################################################################################${NC}"
  echo -e "${GREEN}####################################################################################################${NC}"
  echo ""
  echo ""
}

print_update_message() {
  echo -e "${GREEN}$1${NC}"
}

print_error_message() {
  echo -e "${RED}$1${NC}"
}
